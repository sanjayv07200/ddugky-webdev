//ARITHMATIC OPERATOR 
var num1 = 10;
var num2 = 2;
var res1 = num1 + num2; // 12
var res2 = num1 - num2; // 8
var res3 = num1 * num2; // 20
var res4 = num1 / num2; // 5
var res5 = num1 % num2; // 0
var res6 = num2 ** 2;   // 4
console.log(Math.floor(num1/3))
console.log(res1, res2, res3, res4, res5, res6);

